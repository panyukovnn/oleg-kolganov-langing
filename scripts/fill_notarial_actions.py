def main():
    changed, skipped, missing = [], [], []
    for fname, c in PAGES.items():
        path = os.path.join(BASE, fname)
        if not os.path.isfile(path):
            missing.append(fname)
            continue
        html = open(path, encoding="utf-8").read()
        if 'class="kn-prose"' in html:
            skipped.append(fname)
            continue
        if ANCHOR not in html:
            missing.append(fname + " (anchor not found)")
            continue
        new_block = section(c["lead"], c["what"], c["when"], c["docs"], c["timing"])
        html = html.replace(ANCHOR, new_block, 1)
        open(path, "w", encoding="utf-8").write(html)
        changed.append(fname)
    print("CHANGED (%d): %s" % (len(changed), ", ".join(changed)))
    print("SKIPPED (%d): %s" % (len(skipped), ", ".join(skipped)))
    print("MISSING (%d): %s" % (len(missing), ", ".join(missing)))


if __name__ == "__main__":
    main()
